package patternProgramming;

public class pattern
{
    public static void main(String[] args)
    {
        int line=3;
        int star=2;
      for (int i=1;i<=line;i++)
        {
            for (int j=1;j<=star;j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
